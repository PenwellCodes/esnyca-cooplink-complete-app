-- Chats
CREATE TABLE dbo.Chats (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Chats PRIMARY KEY DEFAULT NEWID(),
  IsGroup BIT NOT NULL CONSTRAINT DF_Chats_IsGroup DEFAULT (0),
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_Chats_CreatedAt DEFAULT SYSUTCDATETIME()
);

CREATE TABLE dbo.ChatParticipants (
  ChatId UNIQUEIDENTIFIER NOT NULL,
  UserId UNIQUEIDENTIFIER NOT NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_ChatParticipants_CreatedAt DEFAULT SYSUTCDATETIME(),
  CONSTRAINT PK_ChatParticipants PRIMARY KEY (ChatId, UserId),
  CONSTRAINT FK_ChatParticipants_Chat FOREIGN KEY (ChatId) REFERENCES dbo.Chats(Id),
  CONSTRAINT FK_ChatParticipants_User FOREIGN KEY (UserId) REFERENCES dbo.Users(Id)
);

CREATE TABLE dbo.ChatMessages (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_ChatMessages PRIMARY KEY DEFAULT NEWID(),
  ChatId UNIQUEIDENTIFIER NOT NULL,
  SenderUserId UNIQUEIDENTIFIER NOT NULL,
  ReceiverUserId UNIQUEIDENTIFIER NULL,
  Type NVARCHAR(16) NOT NULL CONSTRAINT DF_ChatMessages_Type DEFAULT N'text',
  Text NVARCHAR(MAX) NULL,
  FileUrl NVARCHAR(2048) NULL,
  FileName NVARCHAR(255) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_ChatMessages_CreatedAt DEFAULT SYSUTCDATETIME(),
  ReadAt DATETIME2 NULL,
  CONSTRAINT FK_ChatMessages_Chat FOREIGN KEY (ChatId) REFERENCES dbo.Chats(Id),
  CONSTRAINT FK_ChatMessages_Sender FOREIGN KEY (SenderUserId) REFERENCES dbo.Users(Id),
  CONSTRAINT FK_ChatMessages_Receiver FOREIGN KEY (ReceiverUserId) REFERENCES dbo.Users(Id)
);

CREATE INDEX IX_ChatMessages_ChatId_CreatedAt ON dbo.ChatMessages (ChatId, CreatedAt);

-- Stories
CREATE TABLE dbo.Stories (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Stories PRIMARY KEY DEFAULT NEWID(),
  UserId UNIQUEIDENTIFIER NOT NULL,
  ImageUrl NVARCHAR(2048) NOT NULL,
  Caption NVARCHAR(255) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_Stories_CreatedAt DEFAULT SYSUTCDATETIME(),
  ExpiresAt DATETIME2 NOT NULL,
  CONSTRAINT FK_Stories_User FOREIGN KEY (UserId) REFERENCES dbo.Users(Id)
);

CREATE TABLE dbo.StoryViews (
  StoryId UNIQUEIDENTIFIER NOT NULL,
  ViewerUserId UNIQUEIDENTIFIER NOT NULL,
  ViewedAt DATETIME2 NOT NULL CONSTRAINT DF_StoryViews_ViewedAt DEFAULT SYSUTCDATETIME(),
  CONSTRAINT PK_StoryViews PRIMARY KEY (StoryId, ViewerUserId),
  CONSTRAINT FK_StoryViews_Story FOREIGN KEY (StoryId) REFERENCES dbo.Stories(Id),
  CONSTRAINT FK_StoryViews_User FOREIGN KEY (ViewerUserId) REFERENCES dbo.Users(Id)
);

