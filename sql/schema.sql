-- Our Story (single-row table; allow multiple rows if you want history)
CREATE TABLE dbo.OurStory (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_OurStory PRIMARY KEY DEFAULT NEWID(),
  Story NVARCHAR(MAX) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_OurStory_CreatedAt DEFAULT SYSUTCDATETIME()
