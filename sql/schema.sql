-- Users
CREATE TABLE dbo.Users (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Users PRIMARY KEY DEFAULT NEWID(),
  Email NVARCHAR(320) NOT NULL,
  PasswordHash NVARCHAR(255) NOT NULL,
  Role NVARCHAR(32) NOT NULL CONSTRAINT DF_Users_Role DEFAULT N'individual',
  DisplayName NVARCHAR(120) NOT NULL,
  PhoneNumber NVARCHAR(40) NULL,
  Region NVARCHAR(40) NULL,
  RegistrationNumber NVARCHAR(80) NULL,
  PhysicalAddress NVARCHAR(255) NULL,
  Content NVARCHAR(MAX) NULL,
  ProfilePicUrl NVARCHAR(2048) NULL,
  CompanyAddress NVARCHAR(255) NULL,
  LocationLat FLOAT NULL,
  LocationLng FLOAT NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_Users_CreatedAt DEFAULT SYSUTCDATETIME(),
  UpdatedAt DATETIME2 NOT NULL CONSTRAINT DF_Users_UpdatedAt DEFAULT SYSUTCDATETIME()
);

CREATE UNIQUE INDEX UX_Users_Email ON dbo.Users (Email);
CREATE UNIQUE INDEX UX_Users_RegistrationNumber_NotNull
  ON dbo.Users (RegistrationNumber)
  WHERE RegistrationNumber IS NOT NULL;

-- News
CREATE TABLE dbo.News (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_News PRIMARY KEY DEFAULT NEWID(),
  Title NVARCHAR(255) NOT NULL,
  Summary NVARCHAR(MAX) NULL,
  Content NVARCHAR(MAX) NOT NULL,
  ImageUrl NVARCHAR(2048) NULL,
  Author NVARCHAR(120) NULL,
  Published BIT NOT NULL CONSTRAINT DF_News_Published DEFAULT (0),
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_News_CreatedAt DEFAULT SYSUTCDATETIME()
);

-- Partners
CREATE TABLE dbo.Partners (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_Partners PRIMARY KEY DEFAULT NEWID(),
  Title NVARCHAR(255) NOT NULL,
  Description NVARCHAR(MAX) NULL,
  ImageUrl NVARCHAR(2048) NULL,
  FacebookUrl NVARCHAR(2048) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_Partners_CreatedAt DEFAULT SYSUTCDATETIME()
);

-- Team members
CREATE TABLE dbo.TeamMembers (
  Id UNIQUEIDENTIFIER NOT NULL CONSTRAINT PK_TeamMembers PRIMARY KEY DEFAULT NEWID(),
  Name NVARCHAR(120) NOT NULL,
  Title NVARCHAR(255) NULL,
  Description NVARCHAR(MAX) NULL,
  Bio NVARCHAR(MAX) NULL,
  ImageUrl NVARCHAR(2048) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_TeamMembers_CreatedAt DEFAULT SYSUTCDATETIME()
);

-- Mission & Vision (single-row table; allow multiple rows if you want history)
CREATE TABLE dbo.MissionVision (
