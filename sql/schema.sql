  Title NVARCHAR(255) NOT NULL,
  Description NVARCHAR(MAX) NULL,
  ImageUrl NVARCHAR(2048) NULL,
  FacebookUrl NVARCHAR(2048) NULL,
  CreatedAt DATETIME2 NOT NULL CONSTRAINT DF_AboutUsCards_CreatedAt DEFAULT SYSUTCDATETIME()
